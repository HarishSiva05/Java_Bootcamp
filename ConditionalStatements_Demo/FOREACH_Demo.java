package ConditionalStatements_Demo;

public class FOREACH_Demo {

	public static void main(String[] args) {
		
		// For-Each loop is also called as Enhanced For Loop
		// It is a traversing technique that iterates through every element in an array/collection.
		
		String fruits[] = {"Apple","Orange","Mango"};
		for(String i : fruits) {
			System.out.println(i);
		}
		
		
	}

}
