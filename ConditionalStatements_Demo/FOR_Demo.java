package ConditionalStatements_Demo;

public class FOR_Demo {
	public static void main(String[] args) {
		//Print the Index with Increment Operator
		for(int index =0; index<=10; index++) {
			 System.out.println(index);
		 }
		
		//Print the Index with Decrement Operator
		for(int index =10; index>=0; index--) {
			 System.out.println(index);
		 }
		//Print the Index Decrement by 2 
		for(int index =20; index>=0; index-=2) {
			 System.out.println(index);
		 }
	}
	 

}
